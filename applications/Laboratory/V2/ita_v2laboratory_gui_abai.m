set(hObject, 'String',user_entry);
if isnan (user_entry)
    errordlg ('Please correct you choice', 'Bad Input', 'modal')
    set(hObject,'ForegroundColor','red');
else
    set(hObject,'ForegroundColor','black');
end
end
