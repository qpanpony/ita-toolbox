%==========================================================================
% Material popup callback
%==========================================================================
function Material_popUp_Callback(hObject, ~)
handles = guidata(hObject);
index = get(handles.Material_popUp, 'Value');
switch index
    case {1,3,4},
        set(handles.Thickness_2_txt, 'Visible', 'off');
        set(handles.Thickness_2_edit, 'Visible', 'off');
        set(handles.Thickness_2_edit,'String','0');
        set(handles.txtUnitPlate_2, 'Visible', 'off');
        
        set(handles.Thickness_3_edit, 'Visible', 'off');
        set(handles.Thickness_3_txt, 'Visible', 'off');
        set(handles.txtUnitPlate_3, 'Visible', 'off');
        set(handles.Thickness_3_edit,'String','0');
        
        set(handles.OpenArea_edit, 'Visible', 'off');
        set(handles.OpenArea_txt, 'Visible', 'off');
        set(handles.txtUnitOpen, 'Visible', 'off');
        set(handles.OpenArea_edit,'String','0');
    case {2},
        set(handles.Thickness_2_txt, 'Visible', 'on');
        set(handles.Thickness_2_edit, 'Visible', 'on');
        set(handles.txtUnitPlate_2, 'Visible', 'on');
        set(handles.Thickness_2_edit,'String','1');
        
        set(handles.Thickness_3_edit, 'Visible', 'on');
        set(handles.Thickness_3_txt, 'Visible', 'on');
        set(handles.txtUnitPlate_3, 'Visible', 'on');
        set(handles.Thickness_3_edit,'String','1');
        
        set(handles.txtUnitOpen, 'Visible', 'off');
        set(handles.OpenArea_txt, 'Visible', 'off');
        set(handles.OpenArea_edit, 'Visible', 'off');
        set(handles.OpenArea_edit, 'String', '0');
        
    case {5},
        set(handles.Thickness_2_txt, 'Visible', 'off');
        set(handles.Thickness_2_edit, 'Visible', 'off');
        set(handles.Thickness_2_edit,'String','0');
        set(handles.txtUnitPlate_2, 'Visible', 'off');
        
        set(handles.Thickness_3_edit, 'Visible', 'off');
        set(handles.Thickness_3_txt, 'Visible', 'off');
        set(handles.txtUnitPlate_3, 'Visible', 'off');
        set(handles.Thickness_3_edit,'String','0');
        
        set(handles.txtUnitOpen, 'Visible', 'on');
        set(handles.OpenArea_txt, 'Visible', 'on');
        set(handles.OpenArea_edit, 'Visible', 'on');
        set(handles.OpenArea_edit, 'String', '1000');
end
end
%==========================================================================
% OK Button callback
%==========================================================================
function OkBut_Callback(hObject, ~)
% Werte abspeichern
handles = guidata(hObject);
group = str2double(get(handles.MatID_edit,'String'));%1
allMaterialCell = get(handles.Material_popUp,'String');

Material = allMaterialCell{get(handles.Material_popUp,'value')};%2
thickness = str2double(get(handles.Thickness_1_edit,'String'));%3
thickness2 = str2double(get(handles.Thickness_2_edit,'String')); %4
luft_thickness = str2double(get(handles.Thickness_3_edit,'String'));%5
density = str2double(get(handles.Density_edit,'String'));%6
YoungsModulus = str2double(get(handles.YoungsM_edit,'String'));%7
fftDegree = str2double(get(handles.SignalL_edit,'String'));%8
noOfSource = str2double(get(handles.NumSource_edit,'String'));%9
pathOf = get(handles.Path_edit,'String');%10
offFlaeche = str2double(get(handles.OpenArea_edit,'String'));%11
handles.outputCell = {group, Material, thickness, thickness2,...
    luft_thickness, density, YoungsModulus,...
    fftDegree, noOfSource, pathOf, offFlaeche};

for i1 = 1:length(handles.outputCell)
    if isnan(handles.outputCell{i1})
        handles.outputCell = false;
        break;
    end
end

guidata(hObject, handles);
if length(handles.outputCell)~=1
    uiresume()
end
end
